/*
	Make_Temp_Fname (int iCheckSize,LPCTSTR csBasename,LPTSTR csOutname);

  ARGUMENTS:
	csBasename : If non-NULL, first attempts to store file in same folder as this file.
	 csOutname : A Buffer that will receive the name of the temporary file.  Should be MAX_PATH characters long.

  RETURN VALUE : TRUE on success, FALSE on failure.
*/
Make_Temp_Fname (int iCheckSize,LPCTSTR csBasename,LPTSTR csOutname)
{
	TCHAR	*csFolder=NULL;

	if (csBasename) {
		TCHAR *tcp = _tcsrchr(csBasename,_T('\\'));

		if (tcp) {
			csFolder = _tcsdup (csBasename);
			*_tcsrchr(csFolder,_T('\\')) = _T('\0');
		}
	}
#ifndef _UNIX
	if (!csFolder) {
		csFolder = new TCHAR[_MAX_PATH];
		GetTempPath (_MAX_PATH, csFolder);
	}
	GetTempFileName (csFolder, _T("jpg"), 0, csOutname );
#endif
	if (csFolder) delete csFolder;
	return TRUE;
}




