//
// Get the drive number of the given path name. If the pathname does not
// specify a disk drive, the current drive is used.
//
//  Argument | What It Is/Does
// ===========================================================================
//  path     | Points to a string that names the path name.
//
// Return Value:
//  * Returns the current drive number (1 = A, 2 = B, 3 = C, etc.)
//
int getDrive(const TCHAR *path)
{
	int drive = 0;
#ifndef _UNIX
	/*
	 * Check for a drive name. If one is not provided,
	 * get the current drive.
	 */
	if(_istalpha(path[0]) && path[1] == _T(':'))
		drive = _totupper(path[0]) - _T('A') + 1;
	else
		drive = _getdrive();
#endif
	return(drive);
}

//
// Get the disk free space in bytes.
//
//  Argument | What It Is/Does
// ===========================================================================
//  path     | Points to a string that names the path name.
//
// Return Value:
//  * On success returns the available space on the drive.
//  * Otherwise, returns -1 and sets error to one of the following:
//    ENODST  No such destination file or directory
//
LONGLONG getDiskFree(const TCHAR *path)
{
#ifndef _UNIX
	double avail = 0;
	TCHAR dd[_MAX_PATH];
	ULARGE_INTEGER freeBytesToCaller, totalBytes, totalFreeBytes;

	int drive = getDrive(path);
	if(path[0] == _T('\\') && path[1] == _T('\\')) {
		_tcscpy(dd, path);
		TCHAR *slash = _tcsrchr(dd, _T('\\')); 
		if(slash)
			*(_tcsinc(slash)) = NULL;
	}
	else if( drive == 0 ) {
		/*
		 * If dst is not a UNC format nor a drive based format and
		 * the current directory is a network directory not mapped to any drive.
		 */
		if(!GetCurrentDirectory(_MAX_PATH, dd) )
			errorR( ENODST, -1 );
	}
	else {
		dd[0] = drive - 1 + _T('A');
		dd[1] = _T(':');
		dd[2] = _T('\\');
		dd[3] = NULL;
	}
	/*
	 * Check for free disk space on destination path.
	 */
	if((GetDiskFreeSpaceEx((LPCTSTR) dd,
		(PULARGE_INTEGER) &freeBytesToCaller,
		(PULARGE_INTEGER) &totalBytes,
		(PULARGE_INTEGER) &totalFreeBytes)) != TRUE)
		errorR(ENODST, 0);
	return freeBytesToCaller.QuadPart;
#else
	return(getdiskfree(path));
#endif // _UNIX
}


static int getheaderbitsperpixel (HEADER *hdr)
{
	int		i, iaParts[8], iaBits[8], iBits;

	getheaderparts (hdr, &iaParts[0]);
	getheaderbits (hdr, &iaBits[0]);
	for (i=0, iBits = 0; i<iaParts[0]; i++) iBits += iaBits[i];
	return iBits;
}

BOOL CImgPipe::GetInfo (int *ipW, int *ipH, int *ipBits, double *dpDPI, int *ipMask)
{
	int			i;
	HEADER		*hdr;
	Instance	*inTTask;				// Temporary task pointer.

	if (!(m_task = BeginSource())) return FALSE;
	inTTask = ApplyConversions(m_task);
	if (inTTask) m_task = inTTask;

	if (!(hdr = XTHeader (m_task))) {
		*ipW = m_task->pixels;		*ipH = m_task->scanlines;
		for (i=0, *ipBits = 0; i<*(m_task->parts); i++) *ipBits += m_task->bits[i];
		XTEnd (m_task);
		return FALSE;
	}
	if (ipW) getheaderdimensions (hdr, ipW, ipH);
	if (dpDPI) getheaderfield (hdr, "Origin", "resolution", SP_DOUBLE, (char*)dpDPI);
	if (ipBits) *ipBits = getheaderbitsperpixel (hdr);
	if (ipMask) *ipMask = maskvalid(hdr);
	XTEnd (m_task);
	return TRUE;
}

