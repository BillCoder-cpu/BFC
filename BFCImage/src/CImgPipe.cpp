long MakeFix (double fVal, int remainderBits)
{
	unsigned long	dRval;

	dRval = (int)fVal; 
	fVal -= dRval;
	dRval <<= remainderBits;
	dRval |= (int)(fVal * (1 << remainderBits));
	return (long)dRval;
}

