/*
BOOL CImgPipe::Render (int iW, int iH,unsigned char *ucpImagebuf, int iPitch);

	Writes the image to memory at a specified size (iW,iH).

	int iW,iH;
		Size of the output representation
	usigned char *ucpImagebuf;
		byte pointer to place the input into
	int iPitch;
		number of bytes per line of the destination bitmap

  Return Value:
		TRUE on success, FALSE on failure
*/
BOOL CImgPipe::Render (int iW, int iH, unsigned char *ucpImagebuf, int iPitch)
{
	int				iPixels, iScanlines;
	HEADER			*hdr;
	Instance		*insTemp;
	int				iBits;

	if (!(m_task = XTInitTask (init_readimage,"file",m_sInFileName,"scale",&iW,&iH,ARG_END) )) {
		error->set (ERIS);
		return FALSE;
	}

	insTemp = ApplyConversions (m_task);		// Apply any extra conversions
	if (insTemp) m_task = insTemp;

	hdr = XTHeader (m_task);
	iBits = getheaderbitsperpixel (hdr);

	if (iBits > 8) {			// Make sure that we are reading 8 bits per sample
		insTemp = XTInitTask (init_channel,"link",m_task,"parts","bgr",ARG_END);
		if (insTemp) m_task = insTemp;

		insTemp = XTInitTask (init_convert, "link", m_task, "interleave", "pixel", ARG_END);
		if (insTemp) m_task = insTemp;
	}

	hdr = XTHeader (m_task);
	getheaderdimensions (hdr, &iPixels, &iScanlines);

	iScanlines = min (iScanlines, iH);
	while (iScanlines--) {
		if (XTProcess (m_task, ucpImagebuf)) return FALSE;
		ucpImagebuf += iPitch;
	}
	return TRUE;
}


