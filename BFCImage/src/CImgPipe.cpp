/*
	Reads a word stored in the Motorola byte order from an input stream.
	Works on either Intel or Motorola platform
*/
WORD Motorola_Word ( FILE *fp )
{
	WORD	w,a,b;

	a = getc (fp);
	b = getc (fp);
	w = (b&0xff) << 8;
	w |= (a & 0xff);
	return w;
}

#define	FIELD_BYTE		1
#define	FIELD_ASCII		2
#define	FIELD_SHORT		3
#define	FIELD_LONG		4
#define	FIELD_RATIONAL	5

#define	PHT_GRAY	1
#define	PHT_RGB		2
#define	PHT_INDEXED	3
#define	PHT_CMYK	5
#define	PHT_YCC		6
#define	PHT_LAB		8

#define	TAG_NEW_SUBFILETYPE				0xFE
#define	TAG_IMAGE_WIDTH					0X100
#define	TAG_IMAGE_LENGTH				0X101
#define	TAG_BITS_PER_SAMPLE				0X102
#define	TAG_SAMPLES_PER_PIXEL			0X115
#define	TAG_COMPRESSION					0X103
#define	TAG_PHOTOMETRIC_INTERPRETATION	0X106
#define	TAG_STRIP_OFFSETS				0X111
#define	TAG_ROWS_PER_STRIP				0X116
#define	TAG_STRIP_BYTE_COUNTS			0X117
#define	TAG_XRESOLUTION					0X11A
#define	TAG_YRESOLUTION					0X11B
#define	TAG_RESOLUTION_UNIT				0X128

#define	TAG_JPEG_PROC				0X200
#define	TAG_JPEG_JFIF				0X201
#define	TAG_JPEG_JFIF_LENGTH		0X202
#define	TAG_JPEG_RESTART_INTERVAL	0X203
#define	TAG_JPEG_LOSSLESS_PREDCITOR	0X205
#define	TAG_JPEG_POINT_TRANSFORMS	0X206
#define	TAG_JPEG_QTABLES			0X207		// LOSSY Q TABLES.
#define	TAG_JPEG_DCTABLES			0X208		// LOSSLESS HUFFMAN TABLES.
#define	TAG_JPEG_ACTABLES			0X209		// LOSSY HUFFMAN TABLES.

static void Iputw (WORD w, FILE *fp);
static void Iputl (DWORD dw, FILE *fp);
/* Write out a word in intel byte format */
static void Iputw (WORD w, FILE *fp)
{
	putc (w&0xff, fp);
	putc (w>>8, fp);
}

static void Iputl (DWORD dw, FILE *fp)
{
	Iputw ((WORD)(dw & 0xffff), fp);
	Iputw ((WORD)(dw >>16), fp);
}

static void WriteTag (FILE *fp, int iTag, int iFieldType, int iCount, int iValue, int iValue2=0);
static void WriteTag (FILE *fp, int iTag, int iFieldType, int iCount, int iValue, int iValue2)
{
	int	iFieldLength;

	Iputw (iTag, fp);
	Iputw (iFieldType, fp);
	Iputl (iCount, fp);				// Long
	switch (iFieldType) {
		case	FIELD_BYTE:
			iFieldLength = 1;
			break;
		case	FIELD_ASCII:
			iFieldLength = 1;
			break;
		case	FIELD_SHORT:
			iFieldLength = 2;
			break;
		case	FIELD_LONG:
			iFieldLength = 4;
			break;
		case	FIELD_RATIONAL:
			iFieldLength = 8;
			break;
	}
	if (iFieldLength * iCount <= 4) {		// Value will fit in 4 bytes right here.
		switch (iFieldType) {
			case	FIELD_BYTE:
				iFieldLength = 1;
				break;
			case	FIELD_ASCII:
				iFieldLength = 1;
				break;
			case	FIELD_SHORT:
				Iputw (iValue, fp);
				if (iCount == 2) Iputw (iValue2, fp); else Iputw (0, fp);
				break;
			case	FIELD_LONG:
				Iputl (iValue, fp);
				break;
		}
	} else {
		Iputl (iValue, fp);		// PUT IN THE OFFSET
	}

}


/*
CImgPipe::Make_JPEG_TIFF (LPCTSTR sJPEGFilename, LPCTSTR sTIFFFilename,int iWidth,int iLength,int iDPI);

  Builds a .Tiff header for JPEG file format and then tacks on the JPEG file 
  Makes a TIFF file with JPEG storage compatible with the TIFF 6.0 Spec.

	RETURN VALUE:
		FALSE : Failure
		 TRUE : Success
*/
BOOL CImgPipe::Make_JPEG_TIFF (LPCTSTR sJPEGFilename, LPCTSTR sTIFFFilename,int iWidth,int iLength,int iDPI)
{
	int				i, iJOffset = 0, iMarkLength;
	unsigned char	ucMarker;
	FILE			*sfp, *dfp;
	long			alQOffsets[64], iQCount = 0;		// Quantization tables
	long			alHOffsets[64], iHCount = 0;		// Huffman tables
	long			iNextData, iEndData;
	unsigned char	*buf;
	int				iSamplesPerPixel = 3;
	int				iPht;
	BOOL			bRval = TRUE;

	if (!(sfp = _tfopen (sJPEGFilename, "rb"))) return FALSE;

	fseek (sfp, 0, SEEK_END);
	if (getDiskFree(sTIFFFilename) < 768 + ftell(sfp)) {
		error->set (ENODISK);
		fclose (sfp);
		return FALSE;
	}
	rewind (sfp);

	if (!(dfp = _tfopen (sTIFFFilename, "wb"))) {
		error->set (EDSTACS);
		fclose (sfp);
		return FALSE;
	}

	Iputw (0x4949, dfp);				// Intel byte order
	putc (42, dfp);	putc (0, dfp);		// Tiff
	putc (0x30, dfp); putc (0, dfp);	// 0x30 = offset of first IFD entry
	Iputw (0, dfp);						//    is long word.

	// Now write 0x28 bytes of description from this file

	fprintf (dfp, "%40s", "Created by XEROX image services");

int	TAG_COUNT = 18;
	if (ExportSettings.iOutputCompression() == COMP_JPEG_LOSSLESS) TAG_COUNT--;	// Lossless has 1 less field

	Iputw (TAG_COUNT, dfp);

	WriteTag (dfp, TAG_NEW_SUBFILETYPE, FIELD_LONG, 1, 0);			// JPEG Compression (6)
	WriteTag (dfp, TAG_COMPRESSION, FIELD_SHORT, 1, 6);				// JPEG Compression (6)
	WriteTag (dfp, TAG_IMAGE_WIDTH, FIELD_SHORT, 1, iWidth);
	WriteTag (dfp, TAG_IMAGE_LENGTH, FIELD_SHORT, 1, iLength);
	WriteTag (dfp, TAG_ROWS_PER_STRIP, FIELD_SHORT, 1, iLength);

	iPht = PHT_RGB;
	switch (ExportSettings.iColorSpace()) {
		case CSPACE_YCBCR:
			iPht = PHT_YCC;
			break;
		case CSPACE_CMYK:
			iPht = PHT_CMYK;
			break;
		case CSPACE_LAB:
//			iPht = PHT_LAB;
			iPht = PHT_YCC;
			break;
		case CSPACE_GRAY:
			iPht = PHT_GRAY;
			break;
		case CSPACE_INDEXED_RGB:
			iPht = PHT_INDEXED;
			break;
		case CSPACE_RGB:
			iPht = PHT_RGB;
			break;
	}
	WriteTag (dfp, TAG_PHOTOMETRIC_INTERPRETATION, FIELD_SHORT, 1, iPht);  // 
	WriteTag (dfp, TAG_SAMPLES_PER_PIXEL, FIELD_SHORT, 1, iSamplesPerPixel);

	while (fgetc (sfp) == 0xff) {		// Read the JPEG header.
		ucMarker = fgetc (sfp);
		iJOffset += 2;
		if (ucMarker == 0xD8) continue;		// JPEG identifier
		if (ucMarker == 0xD9) break;		// EOI (End of Image) marker

		iMarkLength = Motorola_Word (sfp);
		switch (ucMarker) {
			case 0xE0:			// JFIF
				break;
			case 0xDB:			// Quantization Table Marker
				alQOffsets[iQCount++] = iJOffset;// Save up these locations and write a single tag to list them.
				break;
			case 0xC4:			// Huffman DC Table Marker
				alHOffsets[iHCount++] = iJOffset;// Save up these locations and write a single tag to list them.
				break;
			case 0xC0:			// Start of Frame Marker
				break;
			case 0xDA:			// Start of Scan Marker
				break;
			case 0xFE:			// Comment Marker
				break;
		}
		iJOffset += iMarkLength;
		fseek (sfp, iMarkLength-2, SEEK_CUR);
	}

	iNextData = (TAG_COUNT * 12) + 4 + 0x32;
	iEndData = iNextData + 16 + (iHCount * 4);
	if (ExportSettings.iOutputCompression() == COMP_JPEG_LOSSY) iEndData += (iQCount * 4);

	if (iSamplesPerPixel == 1)
		WriteTag (dfp, TAG_BITS_PER_SAMPLE, FIELD_SHORT, 1, 8);
	else {
		WriteTag (dfp, TAG_BITS_PER_SAMPLE, FIELD_SHORT, iSamplesPerPixel, iNextData);
		iNextData += 8;
		iEndData += 8;
	}

	WriteTag (dfp, TAG_RESOLUTION_UNIT, FIELD_SHORT, 1, 2);		// Dots per inch
	WriteTag (dfp, TAG_XRESOLUTION, FIELD_RATIONAL, 1, iNextData);	// Dots per inch
	iNextData += 8;
	WriteTag (dfp, TAG_YRESOLUTION, FIELD_RATIONAL, 1, iNextData);	// Dots per inch
	iNextData += 8;

	WriteTag (dfp, TAG_JPEG_PROC, FIELD_SHORT, 1, ExportSettings.iOutputCompression()==COMP_JPEG_LOSSLESS?14:1);	// Baseline Process
	if (ExportSettings.iOutputCompression() == COMP_JPEG_LOSSLESS) 
	{
		WriteTag (dfp, TAG_JPEG_DCTABLES, FIELD_LONG, iHCount, iNextData);
		iNextData += iHCount * 4;
	} else {
		WriteTag (dfp, TAG_JPEG_ACTABLES, FIELD_LONG, iHCount, iNextData);
		iNextData += iHCount * 4;
		WriteTag (dfp, TAG_JPEG_QTABLES, FIELD_LONG, iQCount, iNextData);
		iNextData += iQCount * 4;
	}

	// iJOffset now contains the offset of the start of actual JPEG Data.
	WriteTag (dfp, TAG_STRIP_OFFSETS, FIELD_LONG, 1, iJOffset+iNextData);
	fseek (sfp, 0, SEEK_END);
	WriteTag (dfp, TAG_STRIP_BYTE_COUNTS, FIELD_LONG, 1, ftell(sfp)-iJOffset);

	WriteTag (dfp, TAG_JPEG_JFIF, FIELD_LONG, 1, iEndData);					// Point to SOI
	WriteTag (dfp, TAG_JPEG_JFIF_LENGTH, FIELD_LONG, 1, ftell(sfp));

	Iputl (0, dfp);		// Write out 4 zeros after the last IFD TAG


	//**************************** Now write out the Data *********************************
	if (iSamplesPerPixel > 1) {
		Iputw (8,dfp);		Iputw (8,dfp);		Iputw (8,dfp);		Iputw (8,dfp);
	}
	// xres, yres (RATIONALS)
	Iputl (iDPI,dfp);	 Iputl (1,dfp);		// Numerator, Denominator
	Iputl (iDPI,dfp);	 Iputl (1,dfp);		// Numerator, Denominator
	// DC table
	for (i=0; i<iHCount; i++) Iputl (alHOffsets[i]+iEndData, dfp);
	if (ExportSettings.iOutputCompression() == COMP_JPEG_LOSSY)
		for (i=0; i<iQCount; i++) Iputl (alQOffsets[i]+iEndData, dfp);
	// Now, append the JPEG file to the TIFF
#define	BUFSIZE	512
	buf = new unsigned char[BUFSIZE];
	if (buf) {
		int	count;

		fseek (sfp, 0, SEEK_SET);
		while ((count = fread (buf, 1, BUFSIZE, sfp))) {
			if(fwrite (buf, 1, count, dfp) != (size_t)count) {
				error->set (EIOWR);
				bRval = FALSE;
				break;
			}
		}
		delete buf;
	} else {
		error->set (ENOMEM);
		bRval = FALSE;
	}
	fclose (dfp);
	fclose (sfp);
	return bRval;
}

