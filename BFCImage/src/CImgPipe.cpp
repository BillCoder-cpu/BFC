	if (!Packet->bpBuffer) return FALSE;
	iLine = 0;
	do {
		iScanLines = Packet->iScanLines;
		for (; iLine < iScanLines; iLine++) {
ptfSync:
			if (Packet->bDie) break;		// Die at THE END.
			iReadpoint = Packet->iReadLine - Packet->iKeepLines;
			if (iReadpoint < 0) iReadpoint += BUFFER_LINES;
			if (Packet->iWriteLine == iReadpoint-1) {
				Sleep(10);
				goto ptfSync;
			}
			ucBuffer = Packet->bpBuffer + (Packet->iWriteLine * Packet->iPitch);
			if (!bFailed) bFailed = XTProcess (Packet->task, ucBuffer);
//			WriteFile (Packet->hWriteSourcePipe, ucBuffer, Packet->iPitch, &dwBytesWritten, NULL);
			if (++(Packet->iWriteLine) >= BUFFER_LINES) Packet->iWriteLine = 0;
		}
		WaitForSingleObject( Packet->evDieOrGrow, INFINITE );
	} while (iLine < Packet->iScanLines && !Packet->bDie);
	Packet->bDead = TRUE;
//	CloseHandle (Packet->hWriteSourcePipe);
//	AfxEndThread( 0 );
	return TRUE;
