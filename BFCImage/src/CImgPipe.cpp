/*
	Specify a file to be merged into a CImgPipe's input,
	 and give it's location, rotation, and transparency.
*/
bool CImgPipe::NMerge (_TCHAR *sFilename, int iRotateDegrees, int iXoff, int iYoff, bool bTransparent, double dScale)
{
	int	iIndex = 0;

	while (m_MergeFiles[iIndex].sFileName) iIndex++;
	if (iIndex >= IP_MAX_MERGE-1) return false;

	m_MergeFiles[iIndex].sFileName = _tcsdup(sFilename);	// Fill in info for this list.
	m_MergeFiles[iIndex].iRotateDegrees = iRotateDegrees;
	m_MergeFiles[iIndex].iXoff = iXoff;
	m_MergeFiles[iIndex].iYoff = iYoff;
	m_MergeFiles[iIndex].bTransparent = bTransparent;
	m_MergeFiles[iIndex].dScale = dScale;
	m_MergeFiles[iIndex+1].sFileName = NULL;		// Mark the End of the List
	return true;
}

